        List<Integer> c = Stream.of(bufferedReader.readLine().replaceAll("\\s+$", "").split(" "))
                .map(Integer::parseInt)
                .collect(toList());

        int result = Result4.jumpingOnClouds(c);

        bufferedWriter.write(String.valueOf(result));
